list *listCreate(void){

    struct list *list;

    if ((list = malloc(sizeof(*list))) == NULL)
        return NULL;
	
    list->head = list->tail = NULL;
    list->len = 0;
    list->dup = NULL;
    list->free = NULL;
    list->match = NULL;
    return list;
	 

}

